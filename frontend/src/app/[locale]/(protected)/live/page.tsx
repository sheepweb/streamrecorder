// app/(protected)/video/page.tsx
import { ScopeEnum } from "@/lib/types/strapi-autogenerated";
import {
  dehydrate,
  HydrationBoundary,
  QueryClient,
} from "@tanstack/react-query";
import { fetchLiveRecordings } from "./actions/fetch-live-recordings";
import LiveInfinity from "./components/live-infinity";

interface PageProps {
  searchParams: Promise<{
    scope?: string;
    type?: string;
  }>;
}

export default async function LivePage({ searchParams }: PageProps) {
  const { scope = ScopeEnum.Following, type } = await searchParams;

  const queryClient = new QueryClient();

  await queryClient.prefetchInfiniteQuery({
    queryKey: ["live-recordings", scope, type],
    queryFn: ({ pageParam }) => fetchLiveRecordings(scope, pageParam, type),
    initialPageParam: 1,
  });

  return (
    <HydrationBoundary state={dehydrate(queryClient)}>
      <LiveInfinity scope={scope} type={type} />
    </HydrationBoundary>
  );
}
