"use server";

import api from "@/lib/api";
import {
  ClipShareRequestStateEnum,
  ProviderEnum,
} from "@/lib/types/strapi-autogenerated";

interface ShareToYouTubeInput {
  clipDocumentId: string;
  title: string;
  description: string;
  privacyStatus: string;
  tags: string[];
}

interface ShareResult {
  success: boolean;
  error?: string;
  videoId?: string;
}

async function getYouTubeTokens() {
  const connection = await api.socialAccount.meGetSocialAccounts({
    provider: "youtube" as ProviderEnum,
  });
  const data = connection.data?.data?.[0];
  if (!data?.accessToken) return null;

  let accessToken = data.accessToken;

  // Check if token is expired and refresh if needed
  const expiresAt = new Date(data.expiresAt || 0);
  if (new Date() >= expiresAt) {
    if (!data.refreshToken) {
      return null;
    }
    const refreshResult = await refreshYouTubeToken(
      data.refreshToken,
      data.documentId!,
    );
    if (!refreshResult.success || !refreshResult.accessToken) {
      return null;
    }
    accessToken = refreshResult.accessToken;
  }

  return { accessToken, documentId: data.documentId! };
}

async function refreshYouTubeToken(
  refreshToken: string,
  documentId: string,
): Promise<{ success: boolean; accessToken?: string }> {
  try {
    const response = await fetch("https://oauth2.googleapis.com/token", {
      method: "POST",
      headers: { "Content-Type": "application/x-www-form-urlencoded" },
      body: new URLSearchParams({
        client_id: process.env.YOUTUBE_CLIENT_ID!,
        client_secret: process.env.YOUTUBE_CLIENT_SECRET!,
        grant_type: "refresh_token",
        refresh_token: refreshToken,
      }),
    });

    const data = await response.json();

    if (data.error) {
      console.error("YouTube token refresh failed:", data.error);
      return { success: false };
    }

    const expiresAt = new Date(
      Date.now() + data.expires_in * 1000,
    ).toISOString();

    await api.socialAccount.mePutSocialAccountsId(
      { id: documentId },
      {
        data: {
          accessToken: data.access_token,
          ...(data.refresh_token && { refreshToken: data.refresh_token }),
          expiresAt,
        },
      } as never,
    );

    return { success: true, accessToken: data.access_token };
  } catch (error) {
    console.error("Error refreshing YouTube token:", error);
    return { success: false };
  }
}

export async function shareToYouTube({
  clipDocumentId,
  title,
  description,
  privacyStatus,
  tags,
}: ShareToYouTubeInput): Promise<ShareResult> {
  try {
    // 1. Get YouTube tokens
    const tokens = await getYouTubeTokens();
    if (!tokens) {
      return {
        success: false,
        error: "YouTube account not connected. Please connect in Settings.",
      };
    }

    // 2. Get the clip details
    const clipResponse = await api.clip.meGetClipOne({ id: clipDocumentId });
    const clip = clipResponse.data?.data;
    if (!clip) {
      return { success: false, error: "Clip not found" };
    }

    // 3. Get the video file URL
    const baseUrl = process.env.NEXT_PUBLIC_BASE_URL;
    const videoUrl = `${baseUrl}/clip/${clipDocumentId}/clip.mp4`;

    // 4. Initiate resumable upload to YouTube
    const metadata = {
      snippet: {
        title: title.substring(0, 100),
        description: description.substring(0, 5000),
        tags: tags.length > 0 ? tags : undefined,
        categoryId: "22", // People & Blogs
      },
      status: {
        privacyStatus,
        selfDeclaredMadeForKids: false,
      },
    };

    const initResponse = await fetch(
      "https://www.googleapis.com/upload/youtube/v3/videos?uploadType=resumable&part=snippet,status",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${tokens.accessToken}`,
          "Content-Type": "application/json; charset=UTF-8",
          "X-Upload-Content-Type": "video/mp4",
        },
        body: JSON.stringify(metadata),
      },
    );

    if (!initResponse.ok) {
      const errorData = await initResponse.json().catch(() => null);
      console.error("YouTube init error:", errorData);
      const message =
        errorData?.error?.message || `YouTube API error: ${initResponse.status}`;
      return { success: false, error: message };
    }

    const uploadUrl = initResponse.headers.get("Location");
    if (!uploadUrl) {
      return { success: false, error: "No upload URL returned from YouTube" };
    }

    // 5. Download the clip video
    const videoResponse = await fetch(videoUrl);
    if (!videoResponse.ok) {
      return { success: false, error: "Failed to fetch clip video" };
    }

    // 6. Upload video to YouTube (stream directly to avoid buffering in memory)
    const uploadResponse = await fetch(uploadUrl, {
      method: "PUT",
      headers: {
        Authorization: `Bearer ${tokens.accessToken}`,
        "Content-Type": "video/mp4",
      },
      body: videoResponse.body,
      // @ts-expect-error - duplex required for streaming body in Node.js fetch
      duplex: "half",
    });

    if (!uploadResponse.ok) {
      const errorData = await uploadResponse.json().catch(() => null);
      console.error("YouTube upload error:", errorData);
      return {
        success: false,
        error: errorData?.error?.message || "YouTube upload failed",
      };
    }

    const uploadData = await uploadResponse.json();
    const videoId = uploadData.id;

    // 7. Create clip-share record
    await api.clipShare.mePostClipShares({
      data: {
        clip: clipDocumentId,
        platform: "youtube",
        state: ClipShareRequestStateEnum.Completed,
        data: {
          videoId,
          title,
          privacyStatus,
          publishedAt: new Date().toISOString(),
          youtubeUrl: `https://youtube.com/shorts/${videoId}`,
        },
      },
    });

    return { success: true, videoId };
  } catch (error) {
    console.error("Error sharing to YouTube:", error);
    return {
      success: false,
      error: "Failed to share to YouTube. Please try again.",
    };
  }
}
