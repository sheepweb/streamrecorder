"use client";

import type { HlsVideoElement } from "hls-video-element";
import { Flex, Loader, Modal } from "@mantine/core";
import { useQuery } from "@tanstack/react-query";
import { useParams, useRouter, useSearchParams } from "next/navigation";
import { useCallback, useState } from "react";

import { VideoScrollPlayer } from "@/app/[locale]/(protected)/components/video/video-scroll-player";

import { getRecordingsByIds } from "@/app/[locale]/(protected)/watch-later/actions";
import { getProfileUrl } from "@/app/components/open-social";
import { useWatchLater } from "@/app/hooks/use-watch-later";
import { Recording } from "@/lib/types/strapi-autogenerated";

const PAGE_SIZE = 12;

export default function CreatorRecordingModal() {
  const router = useRouter();
  const params = useParams<{
    id: string;
    username: string;
    type: string;
  }>();
  const searchParams = useSearchParams();
  const { watchLater } = useWatchLater();
  const [visibleCount, setVisibleCount] = useState(PAGE_SIZE);

  // Get the IDs to fetch (paginated)
  const idsToFetch = watchLater.slice(0, visibleCount);

  const { data: recordings = [], isLoading } = useQuery({
    queryKey: ["watch-later-modal", idsToFetch],
    queryFn: () => getRecordingsByIds(idsToFetch),
    enabled: idsToFetch.length > 0,
  });

  // Sort recordings to match watchLater order
  const sortedRecordings = idsToFetch
    .map((id) => recordings.find((r: Recording) => r.documentId === id))
    .filter(Boolean) as Recording[];

  const hasNextPage = visibleCount < watchLater.length;
  const isFetchingNextPage = false;

  const fetchNextPage = useCallback(() => {
    setVisibleCount((prev) => prev + PAGE_SIZE);
  }, []);

  const handleClose = () => {
    document.querySelectorAll<HlsVideoElement>("hls-video").forEach((el) => {
      el.pause();
      if (el.api) {
        el.api.stopLoad();
        el.api.detachMedia();
        el.api.destroy();
        el.api = null;
      }
    });
    setTimeout(() => router.back(), 50);
  };

  const handleVisibleChange = useCallback(
    (recording: Recording) => {
      const basePath = `${getProfileUrl(recording.follower)}/video/${recording.documentId}`;
      const search = searchParams.toString();
      const url = search ? `${basePath}?${search}` : basePath;
      window.history.replaceState(null, "", url);
    },
    [searchParams],
  );

  const handleNotFound = () => {
    router.replace("/watch-later");
  };

  const videoExists = sortedRecordings.some((r) => r.documentId === params.id);

  if (isLoading || (!videoExists && watchLater.includes(params.id))) {
    return (
      <Modal.Root opened={true} onClose={handleClose} fullScreen>
        <Modal.Content>
          <Flex h="100dvh" justify="center" align="center">
            <Loader size="lg" />
          </Flex>
        </Modal.Content>
      </Modal.Root>
    );
  }

  return (
    <Modal.Root
      opened={true}
      onClose={handleClose}
      fullScreen
      styles={{
        body: { height: "100%", padding: 0, overflow: "hidden" },
        content: { overflow: "hidden" },
      }}
    >
      <Modal.Content>
        <Modal.Body p={0} h="100%">
          <VideoScrollPlayer
            recordings={sortedRecordings}
            initialId={params.id}
            hasNextPage={hasNextPage}
            isFetchingNextPage={isFetchingNextPage}
            onFetchNextPage={fetchNextPage}
            onVisibleChange={handleVisibleChange}
            onClose={handleClose}
            onNotFound={handleNotFound}
          />
        </Modal.Body>
      </Modal.Content>
    </Modal.Root>
  );
}
