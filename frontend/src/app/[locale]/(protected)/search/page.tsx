"use client";

import { CreatorUpgradeModal } from "@/app/[locale]/(protected)/components/creator-upgrade-modal";
import { PlatformUpgradeModal } from "@/app/[locale]/(protected)/components/platform-upgrade-modal";
import {
  checkUser,
  PlatformType,
  UserSearchResult,
} from "@/app/actions/check-user";
import { follow } from "@/app/actions/followers";
import { getProfileUrl } from "@/app/components/open-social";
import { trackEvent } from "@/app/lib/analytics";
import { parseUsername } from "@/app/lib/parse-username";
import { streamingPlatforms } from "@/app/lib/streaming-platforms";

import { useUser } from "@/app/providers/user-provider";
import { FollowerTypeEnum } from "@/lib/types/strapi-autogenerated";
import {
  ActionIcon,
  Avatar,
  Badge,
  Box,
  Button,
  Card,
  Center,
  Divider,
  Group,
  Image,
  Paper,
  SimpleGrid,
  Stack,
  Text,
  TextInput,
  Title,
  Tooltip,
  UnstyledButton,
  useMatches,
} from "@mantine/core";
import { useDisclosure } from "@mantine/hooks";
import { notifications } from "@mantine/notifications";
import {
  IconBrandChrome,
  IconCheck,
  IconClipboard,
  IconExternalLink,
  IconSearch,
  IconUsersPlus,
  IconX,
} from "@tabler/icons-react";
import { useTranslations } from "next-intl";
import Link from "next/link";
import { useCallback, useRef, useState } from "react";

const ALL_PLATFORMS: PlatformType[] = [
  "tiktok",
  "twitch",
  "kick",
  "youtube",
  "afreecatv",
  "pandalive",
  "bigo",
  "buzzcast",
];

type SearchStep = "input" | "selectPlatform" | "results";

const PREMIUM_ONLY_PLATFORMS: PlatformType[] = ["tiktok", "bigo"];

export default function Page() {
  const t = useTranslations("protected.search");
  const tCommon = useTranslations("protected.common");
  const user = useUser();
  const isFreeUser = user?.role?.type === "authenticated";
  const [upgradeOpened, { open: openUpgrade, close: closeUpgrade }] =
    useDisclosure(false);
  const [
    platformUpgradeOpened,
    { open: openPlatformUpgrade, close: closePlatformUpgrade },
  ] = useDisclosure(false);
  const [blockedPlatform, setBlockedPlatform] = useState<PlatformType | null>(
    null,
  );
  const [query, setQuery] = useState("");
  const [parsedUsername, setParsedUsername] = useState("");
  const [step, setStep] = useState<SearchStep>("input");
  const [searchResults, setSearchResults] = useState<UserSearchResult[]>([]);
  const [isSearching, setIsSearching] = useState(false);
  const [searchError, setSearchError] = useState<string | null>(null);
  const [isFollowing, setIsFollowing] = useState(false);
  const [followedUser, setFollowedUser] = useState<UserSearchResult | null>(
    null,
  );
  const [selectedPlatform, setSelectedPlatform] = useState<PlatformType | null>(
    null,
  );
  const isMobile = useMatches({
    base: true,
    sm: false,
  });
  const cancelSearchRef = useRef(false);
  const inputRef = useRef<HTMLInputElement>(null);

  const cancelSearch = useCallback(() => {
    cancelSearchRef.current = true;
    setIsSearching(false);
  }, []);

  // Handle form submission
  const handleSubmit = (e?: React.FormEvent, overrideQuery?: string) => {
    e?.preventDefault();

    const q = (overrideQuery ?? query).trim();
    if (!q) return;

    const parsed = parseUsername(q);

    if (!parsed.username) {
      setSearchError(t("search.parseError"));
      return;
    }

    setParsedUsername(parsed.username);
    setSearchError(null);
    setSearchResults([]);
    setFollowedUser(null);

    // If URL detected, search that platform directly
    if (parsed.platform) {
      if (
        isFreeUser &&
        PREMIUM_ONLY_PLATFORMS.includes(parsed.platform as PlatformType)
      ) {
        setBlockedPlatform(parsed.platform as PlatformType);
        openPlatformUpgrade();
        return;
      }
      setSelectedPlatform(parsed.platform);
      executeSearch(parsed.username, parsed.platform);
    } else {
      // Show platform selection
      setStep("selectPlatform");
    }
  };

  // Execute search for a specific platform
  const executeSearch = async (username: string, platform: PlatformType) => {
    cancelSearchRef.current = false;
    setStep("results");
    setIsSearching(true);
    setSearchError(null);
    setSearchResults([]);

    try {
      const result = await checkUser(username, platform);

      if (cancelSearchRef.current) return;

      if (result.success && result.user) {
        setSearchResults([result.user]);
        trackEvent("search", {
          keyword: username,
          platform: platform,
          found: "yes",
        });
      } else {
        setSearchError(t("search.notFound"));
        trackEvent("search", {
          keyword: username,
          platform: platform,
          found: "no",
        });
      }
    } catch {
      setSearchError(t("search.notFound"));
    } finally {
      setIsSearching(false);
    }
  };

  // Reset to input step
  const resetSearch = () => {
    setQuery("");
    setParsedUsername("");
    setStep("input");
    setSearchResults([]);
    setSearchError(null);
    setFollowedUser(null);
    setSelectedPlatform(null);
    cancelSearchRef.current = true;
    setTimeout(() => inputRef.current?.focus(), 0);
  };

  // Handle clicking on a user result
  const handleUserSelect = async (user: UserSearchResult) => {
    cancelSearch();
    setSearchResults([user]);
    setIsFollowing(true);

    trackEvent("follow", {
      platform: user.type,
    });

    const result = await follow(user.username, user.type as FollowerTypeEnum);

    setIsFollowing(false);

    if (result.success) {
      setFollowedUser(user);
    } else if (result.error === "MAX_3_FOLLOWERS") {
      openUpgrade();
    } else if (result.error === "MAX_50_FOLLOWERS") {
      notifications.show({
        title: tCommon("follow.errorTitle"),
        message: tCommon("followers.max50Message"),
        color: "red",
      });
    } else if (result.error === "MAX_100_FOLLOWERS") {
      notifications.show({
        title: tCommon("follow.errorTitle"),
        message: tCommon("followers.max100Message"),
        color: "red",
      });
    } else {
      notifications.show({
        title: tCommon("follow.errorTitle"),
        message: result.error,
        color: "red",
      });
    }
  };

  // Get platform info for display
  const getPlatformInfo = (platform: PlatformType) => {
    return streamingPlatforms.find(
      (p) => p.name.toLowerCase() === platform.toLowerCase(),
    );
  };

  // Handle platform selection
  const handlePlatformSelect = (platform: PlatformType) => {
    setSelectedPlatform(platform);
    executeSearch(parsedUsername, platform);
  };

  // Render platform selection buttons
  const renderPlatformSelection = () => {
    return (
      <SimpleGrid cols={{ base: 2, xs: 3, sm: 4 }} spacing="sm">
        {ALL_PLATFORMS.map((platform) => {
          const info = getPlatformInfo(platform);
          const isSelected = selectedPlatform === platform;
          const isLocked =
            isFreeUser && PREMIUM_ONLY_PLATFORMS.includes(platform);
          return (
            <Tooltip
              key={platform}
              label={t("search.premiumOnly")}
              disabled={!isLocked}
            >
              <Button
                variant={isSelected ? "filled" : "default"}
                size="md"
                radius="md"
                loading={isSelected && isSearching}
                disabled={isLocked}
                leftSection={
                  info && (
                    <Image
                      src={info.file}
                      alt={info.name}
                      w={20}
                      h={20}
                      style={{ filter: "brightness(0) invert(1)" }}
                    />
                  )
                }
                rightSection={
                  isLocked ? (
                    <Badge size="xs" color="yellow" variant="filled">
                      PRO
                    </Badge>
                  ) : undefined
                }
                onClick={() => handlePlatformSelect(platform)}
              >
                {t(`platforms.${platform}`)}
              </Button>
            </Tooltip>
          );
        })}
      </SimpleGrid>
    );
  };

  // Render the search results
  const renderContent = () => {
    // Only show content when not in input step
    if (step === "input") return null;

    return (
      <>
        <Card p="md" radius="lg" withBorder bg="transparent">
          <Stack gap="sm">
            {/* Header */}
            <Group justify="space-between" align="center">
              <Text size="md" c="white" fw={500}>
                {step === "results" && selectedPlatform
                  ? t(`platforms.${selectedPlatform}`)
                  : t("search.selectPlatform")}
              </Text>
              <Badge variant="outline" size="sm">
                @{parsedUsername}
              </Badge>
            </Group>

            {renderPlatformSelection()}
          </Stack>
        </Card>

        {/* Results section */}
        {step === "results" && (
          <>
            <Box pos="relative">
              <Stack w="100%">
                {searchResults.map((user) => {
                  const isFollowed =
                    followedUser?.username === user.username &&
                    followedUser?.type === user.type;

                  if (isFollowed) {
                    return (
                      <Paper
                        p="sm"
                        radius="lg"
                        withBorder
                        key={`${user.type}-${user.username}`}
                      >
                        <Group wrap="nowrap" w="100%">
                          <Avatar
                            src={`/api/image/${user.avatar}`}
                            alt={user.nickname}
                            size={60}
                          />

                          <div style={{ flex: 1 }}>
                            <Text fw={500}>{user.nickname}</Text>
                            <Text opacity={0.6} size="xs">
                              @{user.username}
                            </Text>
                            <Badge variant="outline" color="white">
                              {t(`platforms.${user.type}`)}
                            </Badge>
                          </div>

                          <Badge
                            variant="filled"
                            size="lg"
                            color="green"
                            leftSection={<IconCheck size={18} />}
                            style={{ pointerEvents: "none" }}
                          >
                            {t("actions.successTitle")}
                          </Badge>
                        </Group>

                        <Text size="xs" c="dimmed" ta="center" mt="xs">
                          {t("actions.willRecord")}
                        </Text>
                        <Center mt="xs">
                          <Button
                            component={Link}
                            href={getProfileUrl(user as never)}
                            variant="subtle"
                            size="compact-sm"
                          >
                            {t("actions.viewPage")}
                          </Button>
                        </Center>
                      </Paper>
                    );
                  }

                  return (
                    <UnstyledButton
                      key={`${user.type}-${user.username}`}
                      onClick={() => handleUserSelect(user)}
                      disabled={isFollowing}
                      w="100%"
                    >
                      <Paper
                        p="sm"
                        radius="lg"
                        withBorder
                        key={`${user.type}-${user.username}`}
                      >
                        <Group wrap="nowrap" w="100%">
                          <Avatar
                            src={`/api/image/${user.avatar}`}
                            alt={user.nickname}
                            size={60}
                          />

                          <div style={{ flex: 1 }}>
                            <Text fw={500}>{user.nickname}</Text>
                            <Text opacity={0.6} size="xs">
                              @{user.username}
                            </Text>
                            <Badge variant="outline" color="white">
                              {t(`platforms.${user.type}`)}
                            </Badge>
                          </div>

                          <Badge
                            variant="filled"
                            size="lg"
                            leftSection={<IconUsersPlus size={18} />}
                            style={{ pointerEvents: "none" }}
                          >
                            {t("actions.follow")}
                          </Badge>
                        </Group>
                      </Paper>
                    </UnstyledButton>
                  );
                })}

                {!isSearching && searchError && searchResults.length === 0 && (
                  <Text size="sm" c="dimmed" ta="center" py="md">
                    {searchError}
                  </Text>
                )}
              </Stack>
            </Box>
          </>
        )}

        {/* Action buttons */}
        <Group justify="center" gap="sm">
          {step === "selectPlatform" && (
            <Button
              variant="subtle"
              color="gray"
              size="compact-sm"
              onClick={resetSearch}
            >
              {t("search.changeUsername")}
            </Button>
          )}
          {step === "results" && (
            <Button
              variant="light"
              size="sm"
              leftSection={<IconSearch size={16} />}
              onClick={resetSearch}
            >
              {t("search.newSearch")}
            </Button>
          )}
        </Group>
      </>
    );
  };

  return (
    <>
      <CreatorUpgradeModal opened={upgradeOpened} onClose={closeUpgrade} />
      <PlatformUpgradeModal
        opened={platformUpgradeOpened}
        onClose={closePlatformUpgrade}
        platform={
          blockedPlatform
            ? t(`platforms.${blockedPlatform}`)
            : undefined
        }
        platformIcon={
          blockedPlatform
            ? getPlatformInfo(blockedPlatform)?.file
            : undefined
        }
      />

      <Stack w="100%">
        <Group justify="space-between" w="100%">
          <Stack gap={2}>
            <Group gap="xs">
              <IconSearch size={32} />
              <Title order={1} size="h3">
                {t("title")}
              </Title>
            </Group>
            <Text size="sm" c="dimmed">
              {t("description")}
            </Text>
          </Stack>
          <Group gap="lg" justify="center">
            {streamingPlatforms.map((platform) => (
              <Tooltip key={platform.name} label={platform.name}>
                <Image
                  src={platform.file}
                  alt={platform.name}
                  w={24}
                  h={24}
                  style={{ opacity: 0.5, filter: "brightness(0) invert(1)" }}
                />
              </Tooltip>
            ))}
          </Group>
        </Group>

        <Divider mx={{ base: "-xs", sm: "-md" }} />

        {!isMobile && (
          <Paper
            component="a"
            href="https://chromewebstore.google.com/detail/livestreamrecorder/jdofemnojahamgnbncleoekppkddnahi?utm_source=item-share-cb"
            target="_blank"
            rel="noopener noreferrer"
            p="sm"
            radius="lg"
            withBorder
            style={{
              cursor: "pointer",
              textDecoration: "none",
            }}
          >
            <Group gap="sm" justify="center">
              <IconBrandChrome size={20} />
              <Text size="sm" fw={500}>
                {t("chromeExtension")}
              </Text>
              <Badge size="sm" color="green" variant="filled">
                NEW
              </Badge>
              <IconExternalLink size={14} opacity={0.5} />
            </Group>
          </Paper>
        )}

        <Card
          p={{ base: "xs", sm: "lg" }}
          radius="lg"
          withBorder
          bg="transparent"
        >
          <form onSubmit={handleSubmit}>
            <Stack gap="xs">
              <Text size="md" c="white" fw={500}>
                {t("search.enterUsername")}
              </Text>
              <Group gap="xs" align="flex-end">
                <TextInput
                  ref={inputRef}
                  flex={1}
                  value={query}
                  c="white"
                  size={isMobile ? "sm" : "md"}
                  radius="md"
                  autoFocus
                  disabled={step !== "input"}
                  onChange={(e) => setQuery(e.currentTarget.value)}
                  onPaste={(e) => {
                    const text = e.clipboardData.getData("text");
                    if (text?.trim()) {
                      e.preventDefault();
                      setQuery(text);
                      handleSubmit(undefined, text);
                    }
                  }}
                  rightSectionPointerEvents="auto"
                  rightSectionWidth={
                    step !== "input" ? 42 : query.trim().length === 0 ? 100 : 42
                  }
                  rightSection={
                    step !== "input" ? (
                      <ActionIcon
                        variant="subtle"
                        size={isMobile ? "sm" : "lg"}
                        radius={isMobile ? "md" : "xl"}
                        color="gray.6"
                        onClick={resetSearch}
                      >
                        <IconX />
                      </ActionIcon>
                    ) : query.trim().length === 0 ? (
                      <Button
                        variant="subtle"
                        size={isMobile ? "compact-sm" : "compact-lg"}
                        color="gray.6"
                        leftSection={<IconClipboard size={14} />}
                        onClick={async () => {
                          try {
                            const text = await navigator.clipboard.readText();
                            if (text?.trim()) {
                              setQuery(text);
                              handleSubmit(undefined, text);
                            }
                          } catch {
                            notifications.show({
                              message: t("search.pasteError"),
                              color: "yellow",
                            });
                          }
                        }}
                      >
                        {t("search.pasteButton")}
                      </Button>
                    ) : (
                      <ActionIcon
                        variant="subtle"
                        size={isMobile ? "sm" : "lg"}
                        radius={isMobile ? "md" : "xl"}
                        color="gray.6"
                        onClick={() => setQuery("")}
                      >
                        <IconX />
                      </ActionIcon>
                    )
                  }
                />
                {step === "input" && (
                  <Button
                    type="submit"
                    size={isMobile ? "sm" : "md"}
                    radius="md"
                    disabled={!query.trim()}
                    leftSection={<IconSearch size={isMobile ? 14 : 20} />}
                  >
                    {t("search.submitButton")}
                  </Button>
                )}
              </Group>

              {step === "input" && (
                <Group gap="xs">
                  <Text size="xs" c="dimmed">
                    {t("hints.tryFormats")}
                  </Text>
                  <Badge
                    variant="outline"
                    style={{ cursor: "pointer" }}
                    onClick={() => {
                      setQuery("mrbeast");
                      handleSubmit(undefined, "mrbeast");
                    }}
                  >
                    mrbeast
                  </Badge>
                  <Badge
                    variant="outline"
                    style={{ cursor: "pointer" }}
                    onClick={() => {
                      setQuery("https://www.tiktok.com/@mrbeast");
                      handleSubmit(
                        undefined,
                        "https://www.tiktok.com/@mrbeast",
                      );
                    }}
                  >
                    https://www.tiktok.com/@mrbeast
                  </Badge>
                </Group>
              )}

              {searchError && step === "input" && (
                <Text size="sm" c="red" ta="center">
                  {searchError}
                </Text>
              )}
            </Stack>
          </form>
        </Card>

        {renderContent()}
      </Stack>
    </>
  );
}
