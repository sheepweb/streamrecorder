"use client";

import { useUser } from "@/app/providers/user-provider";
import {
  FollowerTypeEnum,
  Recording,
  SourceStateEnum,
} from "@/lib/types/strapi-autogenerated";
import { Anchor, Badge, Box, Button, CopyButton } from "@mantine/core";
import { useTranslations } from "next-intl";
import Link from "next/link";
import { useSearchParams } from "next/navigation";
import { useEffect, useRef, useState } from "react";

import {
  getProfileUrl,
  SOCIAL_URL_PATTERNS,
} from "@/app/components/open-social";
import { useIsNew } from "@/app/providers/is-new-provider";
import { getImageUrl } from "@/app/lib/media-url";
import Image from "next/image";
import { FollowerTypeIcon } from "./follower-type-icon";
import { SpritePreview } from "./sprite-preview";
import { formatDuration } from "./video/player-utils";

interface Props {
  recording: Recording;
  username: string;
  type: FollowerTypeEnum;
}

export function ImageSpritePreview({ recording, type, username }: Props) {
  const { isNew } = useIsNew();
  const t = useTranslations("protected.common");

  const user = useUser();
  const searchParams = useSearchParams();
  const [showVideo, setShowVideo] = useState(false);
  const timeoutRef = useRef<NodeJS.Timeout | null>(null);

  const sources = recording.sources;
  const totalDuration =
    sources?.reduce((sum, s) => sum + (s.duration || 0), 0) || 0;

  const isRecording = sources?.some(
    (s) =>
      s.state === SourceStateEnum.Recording ||
      s.state === SourceStateEnum.Uploading,
  );

  const hasSources = sources && sources.length > 0;
  const lastSource = sources?.at(-1);
  const firstDoneSource = sources?.find((s) => s.state === "done");

  const getHref = (time?: number) => {
    const params = new URLSearchParams(searchParams.toString());
    if (time && time > 0) {
      params.set("t", Math.floor(time).toString());
    }
    return `${getProfileUrl({ username, type })}/video/${
      recording.documentId
    }?${params.toString()}`;
  };

  // Clear hover timeout if component unmounts or user navigates away
  useEffect(() => {
    return () => {
      if (timeoutRef.current) {
        clearTimeout(timeoutRef.current);
      }
    };
  }, []);

  const handleMouseEnter = () => {
    if (!sources || sources.length === 0 || isRecording) return;
    timeoutRef.current = setTimeout(() => {
      setShowVideo(true);
    }, 500);
  };

  const handleMouseLeave = () => {
    if (timeoutRef.current) {
      clearTimeout(timeoutRef.current);
      timeoutRef.current = null;
    }
    setShowVideo(false);
  };

  return (
    <div
      onMouseEnter={handleMouseEnter}
      onMouseLeave={handleMouseLeave}
      style={{
        aspectRatio: "16/9",
        position: "relative",
        overflow: "hidden",
        borderRadius: "var(--mantine-radius-md)",
      }}
    >
      <Anchor
        component={Link}
        prefetch={false}
        href={getHref()}
        onClick={() => {
          if (timeoutRef.current) {
            clearTimeout(timeoutRef.current);
            timeoutRef.current = null;
          }
          setShowVideo(false);
        }}
        style={{
          position: "relative",
          display: "block",
          width: "100%",
          height: "100%",
          pointerEvents: isRecording ? "none" : "auto",
          cursor: isRecording ? "not-allowed" : "pointer",
        }}
      >
        <Image
          alt=""
          src={
            hasSources
              ? isRecording
                ? getImageUrl(recording.documentId!, "screenshot.jpg", lastSource)
                : getImageUrl(recording.documentId!, "preview.jpg", firstDoneSource)
              : "/assets/placeholder/180x280/black/white?text=Recording started"
          }
          unoptimized
          loading="lazy"
          fill
          sizes="(max-width: 768px) 180px, 280px"
          style={{
            borderRadius: "var(--mantine-radius-md)",
            objectFit: "cover",
            objectPosition: "center center",
            ...(isRecording && {
              border: "2px solid red",
            }),
          }}
        />
        {showVideo && !isRecording && sources && sources.length > 0 && (
          <SpritePreview
            baseUrl={`/video/${recording.documentId}/thumbnails.jpg`}
            sources={sources}
          />
        )}
      </Anchor>

      {isRecording && (
        <Badge
          component="a"
          href={SOCIAL_URL_PATTERNS[type]?.(username) || "#"}
          target="_blank"
          rel="noopener noreferrer"
          pos="absolute"
          top={8}
          left={8}
          radius="sm"
          color="red"
          variant="filled"
          size="sm"
          style={{ cursor: "pointer", zIndex: 10 }}
          leftSection={
            <Box
              w={8}
              h={8}
              bg="white"
              style={{ borderRadius: "50%", animation: "pulse 1s infinite" }}
            />
          }
        >
          {t("recordings.recordCount", {
            count: sources && sources?.length > 1 ? `#${sources.length}` : "",
          })}
        </Badge>
      )}

      {isNew(recording) ? (
        <Badge
          radius="md"
          variant="outline"
          size="md"
          color="white"
          bg="green"
          style={{
            position: "absolute",
            bottom: 8,
            left: 8,
          }}
        >
          {t("recordings.newRecording")}
        </Badge>
      ) : null}

      {!isRecording && sources && sources.length > 1 && (
        <div
          style={{
            position: "absolute",
            top: 8,
            left: 8,
            backgroundColor: "rgba(0, 0, 0, 0.8)",
            color: "white",
            padding: "2px 6px",
            borderRadius: 4,
            fontSize: 12,
            fontWeight: 500,
            pointerEvents: "none",
          }}
        >
          {t("recordings.parts", { count: sources.length })}
        </div>
      )}

      <FollowerTypeIcon
        type={type}
        color="black"
        opacity={0.8}
        pos="absolute"
        top={10}
        right={8}
        style={{
          pointerEvents: "none",
        }}
      />

      {isRecording && user?.role?.type === "superadmin" ? (
        <CopyButton
          value={
            recording.sources
              ?.find(
                (s) =>
                  s.state === SourceStateEnum.Recording ||
                  s.state === SourceStateEnum.Uploading,
              )
              ?.executionId?.toString() || ""
          }
        >
          {({ copied, copy }) => (
            <Button
              size="compact-xs"
              color={copied ? "gray" : "black"}
              style={{
                position: "absolute",
                bottom: 8,
                right: 8,
              }}
              onClick={copy}
            >
              {
                recording.sources?.find(
                  (s) =>
                    s.state === SourceStateEnum.Recording ||
                    s.state === SourceStateEnum.Uploading,
                )?.executionId
              }
            </Button>
          )}
        </CopyButton>
      ) : null}

      {totalDuration > 0 && !isRecording && (
        <div
          style={{
            position: "absolute",
            bottom: 8,
            right: 8,
            backgroundColor: "rgba(0, 0, 0, 0.8)",
            color: "white",
            padding: "2px 6px",
            borderRadius: 4,
            fontSize: 12,
            fontWeight: 500,
            pointerEvents: "none",
          }}
        >
          {formatDuration(totalDuration)}
        </div>
      )}
    </div>
  );
}
