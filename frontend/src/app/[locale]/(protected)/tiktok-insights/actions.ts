"use server";

import analyticsApi from "@/lib/analytics-api";
import {
  BattleStat,
  CountryEntry,
  DailyStats,
  GifterRelationship,
  GlobalStats,
  GlobalTopGifter,
  LanguageEntry,
  MostLiveStreamer,
  TopEarner,
} from "@/lib/types/analytics-autogenerated";
import { enrichWithFollowers, FollowerEnrichment } from "./enrich";

export type EnrichedBattleStat = BattleStat & FollowerEnrichment;
export type EnrichedTopEarner = TopEarner & FollowerEnrichment;
export type EnrichedMostLiveStreamer = MostLiveStreamer & FollowerEnrichment;

export type GlobalPage<T> = {
  data: T[];
  total: number;
  hasMore: boolean;
};

export async function getGlobalStats(): Promise<GlobalStats | null> {
  try {
    const res = await analyticsApi.global.getGlobalStats();
    return res.data;
  } catch {
    return null;
  }
}

export async function getGlobalDailyEarnings(
  country?: string,
  language?: string,
): Promise<DailyStats[]> {
  try {
    const res = await analyticsApi.global.getDailyEarnings({ country, language });
    return res.data;
  } catch {
    return [];
  }
}

