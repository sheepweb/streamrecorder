export async function getGlobalTopEarners(
  limit = 10,
  offset = 0,
  country?: string,
  language?: string,
): Promise<GlobalPage<EnrichedTopEarner>> {
  try {
    const res = await analyticsApi.global.getTopStreamers({ limit, offset, country, language });
    const data = await enrichWithFollowers(res.data.data ?? []);
    return { data, total: res.data.total, hasMore: res.data.hasMore };
  } catch {
    return { data: [], total: 0, hasMore: false };
  }
}

