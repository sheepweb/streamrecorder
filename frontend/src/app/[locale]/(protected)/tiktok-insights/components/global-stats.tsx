"use client";

import { GlobalStats } from "@/lib/types/analytics-autogenerated";
import {
  Group,
  Paper,
  SimpleGrid,
  Text,
  ThemeIcon,
} from "@mantine/core";
import {
  IconDiamond,
  IconSword,
  IconUsers,
} from "@tabler/icons-react";
import { useTranslations } from "next-intl";
import { use } from "react";
import { formatCompact } from "../lib";

interface StatCardProps {
  label: string;
  value: number;
  icon: React.FC<{ size?: number }>;
  color: string;
}

function StatCard({ label, value, icon: Icon, color }: StatCardProps) {
  return (
    <Paper withBorder p="md" radius="md">
      <Group justify="space-between" wrap="nowrap">
        <div>
          <Text size="xs" c="dimmed" tt="uppercase" fw={600} lh={1.2}>
            {label}
          </Text>
          <Text fw={700} size="xl" mt={4}>
            {formatCompact(value)}
          </Text>
        </div>
        <ThemeIcon size={44} radius="md" color={color} variant="light">
          <Icon size={22} />
        </ThemeIcon>
      </Group>
    </Paper>
  );
}

interface GlobalStatsWidgetProps {
  statsPromise: Promise<GlobalStats | null>;
}

export function GlobalStatsWidget({ statsPromise }: GlobalStatsWidgetProps) {
  const t = useTranslations("protected.tiktokInsights");
  const stats = use(statsPromise);

  if (!stats) return null;

  return (
    <SimpleGrid cols={{ base: 2, sm: 3 }} spacing="md">
      <StatCard
        label={t("globalStats.totalUsers")}
        value={stats.total_users ?? 0}
        icon={IconUsers}
        color="violet"
      />
      <StatCard
        label={t("globalStats.totalDiamonds")}
        value={stats.total_diamonds ?? 0}
        icon={IconDiamond}
        color="yellow"
      />
      <StatCard
        label={t("globalStats.totalBattles")}
        value={stats.total_battles ?? 0}
        icon={IconSword}
        color="orange"
      />
    </SimpleGrid>
  );
}
