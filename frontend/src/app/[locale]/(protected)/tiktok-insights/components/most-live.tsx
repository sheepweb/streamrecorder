"use client";

import { useIntlNames } from "@/app/[locale]/(protected)/hooks/use-intl-names";
import { getProfileUrl } from "@/app/components/open-social";
import { generateAvatarUrl } from "@/app/lib/avatar-url";
import { FollowerTypeEnum } from "@/lib/types/strapi-autogenerated";
import {
  Anchor,
  Avatar,
  Badge,
  Box,
  Group,
  Image,
  LoadingOverlay,
  Pagination,
  Paper,
  Select,
  SelectProps,
  Table,
  TableScrollContainer,
  TableTbody,
  TableTd,
  TableTh,
  TableThead,
  TableTr,
  Text,
  Title,
} from "@mantine/core";
import { useTranslations } from "next-intl";
import { use, useState } from "react";
import {
  EnrichedMostLiveStreamer,
  getGlobalMostLive,
  GlobalPage,
} from "../actions";

const LIMIT = 10;

const FlagIcon = ({ code, size = 16 }: { code: string; size?: number }) => (
  <Image
    src={`https://flagcdn.com/w40/${code.toLowerCase()}.png`}
    alt={code}
    w={size}
    h={Math.round(size * 0.75)}
    radius={2}
    fit="cover"
  />
);

interface MostLiveProps {
  initialPromise: Promise<GlobalPage<EnrichedMostLiveStreamer>>;
  countriesPromise: Promise<string[]>;
  languagesPromise: Promise<string[]>;
}

export function MostLive({
  initialPromise,
  countriesPromise,
  languagesPromise,
}: MostLiveProps) {
  const t = useTranslations("protected.tiktokInsights");
  const { getCountryName, getLanguageName } = useIntlNames();
  const initialData = use(initialPromise);
  const countries = use(countriesPromise);
  const languages = use(languagesPromise);

  const [streamers, setStreamers] = useState<EnrichedMostLiveStreamer[]>(
    initialData.data,
  );
  const [total, setTotal] = useState(initialData.total);
  const [activePage, setActivePage] = useState(1);
  const [country, setCountry] = useState<string | null>(null);
  const [language, setLanguage] = useState<string | null>(null);
  const [loading, setLoading] = useState(false);

  const totalPages = Math.ceil(total / LIMIT);

  const fetchPage = async (
    page: number,
    c: string | null,
    l: string | null,
  ) => {
    setLoading(true);
    const result = await getGlobalMostLive(
      LIMIT,
      (page - 1) * LIMIT,
      c ?? undefined,
      l ?? undefined,
    );
    setStreamers(result.data);
    setTotal(result.total);
    setLoading(false);
  };

  const handlePageChange = (page: number) => {
    setActivePage(page);
    fetchPage(page, country, language);
  };

  const handleCountryChange = (val: string | null) => {
    const newCountry = val || null;
    setCountry(newCountry);
    setActivePage(1);
    fetchPage(1, newCountry, language);
  };

  const handleLanguageChange = (val: string | null) => {
    const newLanguage = val || null;
    setLanguage(newLanguage);
    setActivePage(1);
    fetchPage(1, country, newLanguage);
  };

  const countryOptions = [
    { value: "", label: t("mostLive.allCountries") },
    ...countries.map((c) => ({ value: c, label: getCountryName(c) })),
  ];

  const languageOptions = [
    { value: "", label: t("mostLive.allLanguages") },
    ...languages.map((l) => ({ value: l, label: getLanguageName(l) })),
  ];

  const renderCountryOption: SelectProps["renderOption"] = ({ option }) =>
    option.value === "" ? (
      <span>{option.label}</span>
    ) : (
      <Group gap="sm">
        <FlagIcon code={option.value} size={18} />
        <span>{option.label}</span>
      </Group>
    );

  const renderLanguageOption: SelectProps["renderOption"] = ({ option }) =>
    option.value === "" ? (
      <span>{option.label}</span>
    ) : (
      <Group gap="sm">
        <Badge size="xs" variant="light" tt="uppercase" w={36}>
          {option.value}
        </Badge>
        <span>{option.label}</span>
      </Group>
    );

  const header = (
    <Box
      style={{
        background: "linear-gradient(150deg, #B4FFD4, #52E8A0)",
        borderTopLeftRadius: 8,
        borderTopRightRadius: 8,
      }}
      p="md"
    >
      <Group justify="space-between" wrap="wrap" gap="xs">
        <Title order={5} c="black">
          {t("mostLive.title")}
        </Title>
        <Group gap="xs" wrap="nowrap">
          <Select
            size="xs"
            data={countryOptions}
            value={country ?? ""}
            onChange={handleCountryChange}
            renderOption={renderCountryOption}
            styles={{ input: { minWidth: 130 } }}
            comboboxProps={{ withinPortal: true }}
            searchable
          />
          <Select
            size="xs"
            data={languageOptions}
            value={language ?? ""}
            onChange={handleLanguageChange}
            renderOption={renderLanguageOption}
            styles={{ input: { minWidth: 130 } }}
            comboboxProps={{ withinPortal: true }}
            searchable
          />
        </Group>
      </Group>
    </Box>
  );

  if (!streamers.length && !loading) {
    return (
      <Paper withBorder radius="md">
        {header}
        <Box p="md">
          <Text c="dimmed" size="sm">
            {t("mostLive.empty")}
          </Text>
        </Box>
      </Paper>
    );
  }

  return (
    <Paper withBorder radius="md">
      {header}
      <Box style={{ position: "relative" }}>
        <LoadingOverlay
          visible={loading}
          zIndex={1}
          overlayProps={{ radius: "sm", blur: 2 }}
        />
        <TableScrollContainer minWidth={800}>
          <Table striped highlightOnHover fz="sm">
            <TableThead>
              <TableTr>
                <TableTh>#</TableTh>
                <TableTh>{t("mostLive.username")}</TableTh>
                <TableTh>{t("mostLive.country")}</TableTh>
                <TableTh>{t("mostLive.language")}</TableTh>
                <TableTh>{t("mostLive.sessions")}</TableTh>
                <TableTh>{t("mostLive.hours")}</TableTh>
              </TableTr>
            </TableThead>
            <TableTbody>
              {streamers.map((s, i) => (
                <TableTr key={s.owner_username ?? i}>
                  <TableTd>
                    <Text size="sm" c="dimmed">
                      {(activePage - 1) * LIMIT + i + 1}
                    </Text>
                  </TableTd>
                  <TableTd>
                    <Group gap="xs" wrap="nowrap">
                      <Avatar
                        src={
                          s.followerAvatarUrl
                            ? generateAvatarUrl(s.followerAvatarUrl)
                            : undefined
                        }
                        size="sm"
                        radius="xl"
                        color="green"
                      >
                        {(s.owner_username ?? "?")[0].toUpperCase()}
                      </Avatar>
                      <div>
                        {s.owner_username ? (
                          <Anchor
                            href={getProfileUrl({
                              username: s.owner_username,
                              type: FollowerTypeEnum.Tiktok,
                            })}
                            size="sm"
                            fw={500}
                          >
                            @{s.owner_username}
                          </Anchor>
                        ) : (
                          <Text size="sm" c="dimmed">
                            —
                          </Text>
                        )}
                        {s.followerNickname && (
                          <Text size="xs" c="dimmed">
                            {s.followerNickname}
                          </Text>
                        )}
                      </div>
                    </Group>
                  </TableTd>
                  <TableTd>
                    {s.owner_country &&
                    /^[A-Za-z]{2}$/.test(s.owner_country) ? (
                      <Group gap={6} wrap="nowrap">
                        <FlagIcon code={s.owner_country} size={16} />
                        <Text size="sm">{getCountryName(s.owner_country)}</Text>
                      </Group>
                    ) : (
                      <Text size="sm" c="dimmed">
                        —
                      </Text>
                    )}
                  </TableTd>
                  <TableTd>
                    {s.owner_language ? (
                      <Group gap={6} wrap="nowrap">
                        <Badge size="xs" variant="light" tt="uppercase">
                          {s.owner_language}
                        </Badge>
                        <Text size="sm">
                          {getLanguageName(s.owner_language)}
                        </Text>
                      </Group>
                    ) : (
                      <Text size="sm" c="dimmed">
                        —
                      </Text>
                    )}
                  </TableTd>
                  <TableTd>
                    <Text size="sm">{s.stream_count ?? 0}</Text>
                  </TableTd>
                  <TableTd>
                    <Text size="sm">{(s.total_hours ?? 0).toFixed(1)}</Text>
                  </TableTd>
                </TableTr>
              ))}
            </TableTbody>
          </Table>
        </TableScrollContainer>
      </Box>
      {totalPages > 1 && (
        <Box p="md" style={{ display: "flex", justifyContent: "center" }}>
          <Pagination
            total={totalPages}
            value={activePage}
            onChange={handlePageChange}
            size="sm"
          />
        </Box>
      )}
    </Paper>
  );
}
