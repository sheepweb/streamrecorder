"use client";

import { generateAvatarUrl } from "@/app/lib/avatar-url";
import { getProfileUrl } from "@/app/components/open-social";
import { FollowerTypeEnum } from "@/lib/types/strapi-autogenerated";
import {
  Anchor,
  Avatar,
  Badge,
  Box,
  Group,
  LoadingOverlay,
  Pagination,
  Paper,
  Table,
  TableScrollContainer,
  TableTbody,
  TableTd,
  TableTh,
  TableThead,
  TableTr,
  Text,
  Title,
} from "@mantine/core";
import { useTranslations } from "next-intl";
import { use, useState } from "react";
import { EnrichedBattleStat, getGlobalBattleStats, GlobalPage } from "../actions";
import { formatCompact } from "../lib";

const LIMIT = 10;

interface BattleStatsProps {
  initialPromise: Promise<GlobalPage<EnrichedBattleStat>>;
}

export function BattleStats({ initialPromise }: BattleStatsProps) {
  const t = useTranslations("protected.tiktokInsights");
  const initialData = use(initialPromise);

  const [stats, setStats] = useState<EnrichedBattleStat[]>(initialData.data);
  const [total, setTotal] = useState(initialData.total);
  const [activePage, setActivePage] = useState(1);
  const [loading, setLoading] = useState(false);

  const totalPages = Math.ceil(total / LIMIT);

  const handlePageChange = async (page: number) => {
    setActivePage(page);
    setLoading(true);
    const result = await getGlobalBattleStats(LIMIT, (page - 1) * LIMIT);
    setStats(result.data);
    setTotal(result.total);
    setLoading(false);
  };

  const header = (
    <Box
      style={{
        background: "linear-gradient(150deg, #FFD4B4, #E88852)",
        borderTopLeftRadius: 8,
        borderTopRightRadius: 8,
      }}
      p="md"
    >
      <Title order={5} c="black">
        {t("battleStats.title")}
        {total > 0 && (
          <Text span size="sm" fw={400} c="dark.3" ml={8}>
            ({total})
          </Text>
        )}
      </Title>
      <Text c="black" size="xs" mt={2}>{t("battleStats.description")}</Text>
    </Box>
  );

  if (!stats.length && !loading) {
    return (
      <Paper withBorder radius="md">
        {header}
        <Box p="md">
          <Text c="dimmed" size="sm">{t("battleStats.empty")}</Text>
        </Box>
      </Paper>
    );
  }

  return (
    <Paper withBorder radius="md">
      {header}
      <Box style={{ position: "relative" }}>
        <LoadingOverlay visible={loading} zIndex={1} overlayProps={{ radius: "sm", blur: 2 }} />
        <TableScrollContainer minWidth={800}>
          <Table striped highlightOnHover fz="sm">
            <TableThead>
              <TableTr>
                <TableTh>#</TableTh>
                <TableTh>{t("battleStats.username")}</TableTh>
                <TableTh>{t("battleStats.battles")}</TableTh>
                <TableTh>{t("battleStats.wins")}</TableTh>
                <TableTh>{t("battleStats.losses")}</TableTh>
                <TableTh>{t("battleStats.draws")}</TableTh>
                <TableTh>{t("battleStats.winRate")}</TableTh>
              </TableTr>
            </TableThead>
            <TableTbody>
              {stats.map((s, i) => (
                <TableTr key={s.owner_username ?? i}>
                  <TableTd>
                    <Text size="sm" c="dimmed">
                      {(activePage - 1) * LIMIT + i + 1}
                    </Text>
                  </TableTd>
                  <TableTd>
                    <Group gap="xs" wrap="nowrap">
                      <Avatar
                        src={s.followerAvatarUrl ? generateAvatarUrl(s.followerAvatarUrl) : undefined}
                        size="sm"
                        radius="xl"
                        color="orange"
                      >
                        {(s.owner_username ?? "?")[0].toUpperCase()}
                      </Avatar>
                      <div>
                        {s.owner_username ? (
                          <Anchor
                            href={getProfileUrl({ username: s.owner_username, type: FollowerTypeEnum.Tiktok })}
                            size="sm"
                            fw={500}
                          >
                            @{s.owner_username}
                          </Anchor>
                        ) : (
                          <Text size="sm" c="dimmed">—</Text>
                        )}
                        {s.followerNickname && (
                          <Text size="xs" c="dimmed">{s.followerNickname}</Text>
                        )}
                      </div>
                    </Group>
                  </TableTd>
                  <TableTd>
                    <Text size="sm">{formatCompact(s.total_battles ?? 0)}</Text>
                  </TableTd>
                  <TableTd>
                    <Text size="sm" c="green" fw={500}>{formatCompact(s.wins ?? 0)}</Text>
                  </TableTd>
                  <TableTd>
                    <Text size="sm" c="red">{formatCompact(s.losses ?? 0)}</Text>
                  </TableTd>
                  <TableTd>
                    <Text size="sm" c="dimmed">{formatCompact(s.draws ?? 0)}</Text>
                  </TableTd>
                  <TableTd>
                    <Badge
                      color={
                        (s.win_rate ?? 0) >= 60
                          ? "green"
                          : (s.win_rate ?? 0) >= 40
                          ? "yellow"
                          : "red"
                      }
                      variant="light"
                      size="sm"
                    >
                      {(s.win_rate ?? 0).toFixed(1)}%
                    </Badge>
                  </TableTd>
                </TableTr>
              ))}
            </TableTbody>
          </Table>
        </TableScrollContainer>
      </Box>
      {totalPages > 1 && (
        <Box p="md" style={{ display: "flex", justifyContent: "center" }}>
          <Pagination total={totalPages} value={activePage} onChange={handlePageChange} size="sm" />
        </Box>
      )}
    </Paper>
  );
}
